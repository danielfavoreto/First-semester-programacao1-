--Exercicio 1--
Em primeira instância, o exercício número 1 fornecia valores constantes para distância e velocidade, para tanto, foi necessária
a inclusão nos parâmetros, a velocidade de 80 e distância de 500.Como já se sabe, a velocidade v de um corpo é calculada pela
fórmula: v=d/t
Onde d é a distancia e t o tempo. Como no exercício a indagação é a respeito do tempo decorrido na viagem, fazendo uso
de ferramentas algébricas, chega-se à conclusão de que o tempo necessário para completar a viagem é:d/v. OU, mais especificamente,
500/80.

